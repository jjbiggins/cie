#include "lec04_functions.h"
#include <iostream>
#include <stdlib.h>

using namespace std;


void arithmeticReview()
{
  float a_f = 9.0;
  float b_f = 10.0;
  int a_i = 9;
  int b_i = 10;

  cout << a_f / b_f << " " << a_i / b_i << " " << b_i % a_i << endl;
}

void ifElseReview()
{
  int x = 3;
  int isY = 1;

  if (x < 2 || !isY)
    {
      cout << "A" << endl;
    }
  else if (isY && x > 0)
    {
      cout << "B" << endl;
    }
  else
    {
      cout << "C" << endl;
    }
}

void whileLoopReview()
{
  int x = 1;
  while (x < 100)
    {
      x = x * 2;
    }

  cout << x << endl;
}

void doWhileLoopReview()
{
  int i = 0;
  do
    {
      i = i + 5;
    }
  while (i <= 15);

  cout << i << endl;
}

void forLoopReview()
{
  int i = 0;
  int x = 0;
  for (i = 0; i < 10; i++)
    {
      x = x + 2;
    }

  cout << i << ", " << x << endl;
}
